class Event {
  final int id;
  final String title;
  final String date;
  final String time;
  final String location;
  final String type;
  final String status;

  Event({
    required this.id,
    required this.title,
    required this.date,
    required this.time,
    required this.location,
    required this.type,
    required this.status,
  });
}
