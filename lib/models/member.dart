class Member {
  final int id;
  final String name;
  final String email;
  final String phone;
  final String role;
  final String department;
  final String joinDate;
  final String status;

  Member({
    required this.id,
    required this.name,
    required this.email,
    required this.phone,
    required this.role,
    required this.department,
    required this.joinDate,
    required this.status,
  });
}
