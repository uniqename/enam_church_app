class Announcement {
  final int id;
  final String title;
  final String content;
  final String priority;
  final String author;
  final String date;
  final String department;
  final String status;

  Announcement({
    required this.id,
    required this.title,
    required this.content,
    required this.priority,
    required this.author,
    required this.date,
    required this.department,
    required this.status,
  });
}
